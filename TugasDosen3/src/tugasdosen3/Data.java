
package tugasdosen3;


public class Data {
      int id;
    String nama;
    int harga;

    public Data(int id, String nama, int harga) {
        this.id = id;
        this.nama = nama;
        this.harga = harga;
    }

    public int getHarga() {
        return harga;
    }

    public int getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }
    
    

   
    
   

}
